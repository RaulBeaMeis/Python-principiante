primer_numero = int(input("Ingresa el primer número: "))
segundo_numero =  int(input("Ingresa el segundo número: "))

#Operaciones aritméticas

print(f'La suma es: {primer_numero} + {segundo_numero}')

print(f'La multiplicación es: {primer_numero * segundo_numero}')


#Operaciones logicas

print(f'¿Son iguales los números? {primer_numero} == {segundo_numero}')

print(f'¿Es el primer número menor que el segundo? {primer_numero} < {segundo_numero}')

print(f'¿Es el segundo numero mayor o igual al primero? {primer_numero} <= {segundo_numero}')