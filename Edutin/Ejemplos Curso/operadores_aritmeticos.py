#Pedir numeros al usuario

numero1 = int(input("Ingresa un número (1): "))
numero2 = int(input("Ingresa un número (2): "))

#Imprimir resultados

#Suma

print(f'La suma es {numero1 + numero2}')

#Resta

print(f'La resta es es {numero1 - numero2}')

#Multiplicación

print(f'La resta es es {numero1 * numero2}')

#División

print(f'La resta es es {numero1 / numero2}')

#Módulo

print(f'La resta es es {numero1 % numero2}')